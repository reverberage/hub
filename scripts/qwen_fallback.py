def main() -> None:
    parser = argparse.ArgumentParser(
        description="Qwen Model Fallback Rotator for opencode",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  %(prog)s --status           # Probe all models, table output\n"
            "  %(prog)s --status --json    # Probe all models, JSON output\n"
            "  %(prog)s --rotate           # Rotate to next available model\n"
            "  %(prog)s --rotate --force   # Force rotate (skip probe)\n"
            "  %(prog)s --rotate --model qwen3-coder-flash  # Set model\n"
            "  %(prog)s --rotate --dry-run # Preview rotation\n"
            "  %(prog)s --track            # Update tracking doc\n"
        ),
    )
    parser.add_argument(
        "--status",
        action="store_true",
        help="Probe all models, output availability table",
    )
    parser.add_argument(
        "--rotate",
        action="store_true",
        help="Test active model; if exhausted, switch to next",
    )
    parser.add_argument(
        "--track",
        action="store_true",
        help="Update tracking doc from current state (no probing)",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Force rotation regardless of current model status",
    )
    parser.add_argument(
        "--json", action="store_true", help="Output as JSON (for automation)"
    )
    parser.add_argument(
        "--model", type=str, help="Override active model directly, skip rotation"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would happen without touching files",
    )

    args = parser.parse_args()

    # Default: --status --json (when no command flag given)
    if not (args.status or args.rotate or args.track):
        args.status = True
        args.json = True

    if args.status:
        cmd_status(args)
    elif args.rotate:
        cmd_rotate(args)
    elif args.track:
        cmd_track(args)


if __name__ == "__main__":
    main()
